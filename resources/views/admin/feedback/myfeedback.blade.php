            var alertMsg = 'One credit will be deducted for sending this email/sms!';
            if (career == 'sms') {
                alertMsg = 'One credit will be deducted for sending this sms!';
            } else {
                alertMsg = 'One credit will be deducted for sending this email!';
            }

            swal({
                title: "Are you sure?",
                text: alertMsg,
                type: "warning",
                showCancelButton: true,
                confirmButtonColor: "#EF5350",
                confirmButtonText: "Yes, send it!",
                cancelButtonText: "No, cancel pls!",
                closeOnConfirm: false,
                closeOnCancel: false
            },
			function (isConfirm) {
				if (isConfirm) {
					var formdata = $("#frmSendFeedbackReply").serialize();
					$('.overlaynew').show();
					$.ajax({
						url: "<?php echo base_url('/admin/feedback/replyFeedback'); ?>",
						type: "POST",
						data: formdata + "&career=" + career,
						dataType: "json",
						success: function (response) {
							$('.overlaynew').hide();
							if (response.status == "success") {
								swal({
									title: "Success!",
									text: response.message,
									confirmButtonColor: "#66BB6A",
									type: "success"
								});
							} else {
								swal({
									title: "ERROR!",
									text: response.message,
									confirmButtonColor: "#2196F3",
									type: "error"
								});
							}
						},
						error: function (response) {
							$('.overlaynew').hide();
							swal({
								title: "ERROR!",
								text: response.message,
								confirmButtonColor: "#2196F3",
								type: "error"
							});
						}
					});
				} else {
					swal({
						title: "Cancelled",
						text: "",
						confirmButtonColor: "#2196F3",
						type: "error"
					});
				}
			});
        });

